"use strict";
//Variable is a container which can store data.//
// Typescript follows the same rules as JavaScript for variables declarations.//
// Variables can be decleared using: Var,let, and const.//
// Var //
var a = 12;
console.log(a);
a = 10;
console.log(a);
a = a + a;
console.log(a);
// Let //
let n = "Fizza";
console.log(n);
n = "Jawed";
console.log(n);
let q = true;
console.log(q);
let e = false;
console.log(e);
// Const //
// variable can be decleared using const similar to var or let declarations.//
// The const make a variable to constant where its value cannot be changed.//
// Const variables must be declared and initialized in a single statement.//
const gpa = 3.5;
console.log(gpa);
// Another Example //
const birthYear = 2002;
console.log(birthYear); // it will console 2002 //
// birthYear = 2003;  => it will show an error to assign 'birthyear' because it is constant.//
